// ── copy helper ───────────────────────────────────────────────────────────────

function CopyBtn({ getText }) {
  const [done, setDone] = useState(false)
  const copy = () => {
    navigator.clipboard.writeText(getText()).then(() => {
      setDone(true)
      setTimeout(() => setDone(false), 1500)
    })
  }
  return (
    <Button size="sm" variant="outline-secondary" onClick={copy}>
      {done ? '✓ Copied' : 'Copy'}
    </Button>
  )
}

// ── public component ──────────────────────────────────────────────────────────

const TABS = ['Pseudocode', 'Arduino C++', 'MicroPython']

export default function CodePanel({ kp, ki, kd, base, ts, platform, sensorCount, highlight = [] }) {
  const [tab, setTab] = useState(0)

  const pseudoText = () => {
    const [lo, hi] = platform?.motor_speed_range || [0, 255]
    const spacing  = platform?.sensor_spacing_mm  || 12
    const center   = ((sensorCount - 1) / 2).toFixed(1)
    const Ts       = (ts / 1000).toFixed(3)
    return [
      `── SETUP ──────────────────────────────────────────`,
      `  Kp = ${kp}   Ki = ${ki}   Kd = ${kd}`,
      `  base_speed = ${base}   Ts = ${ts} ms`,
      `  last_error = 0 ;  integral = 0`,
      ``,
      `── EVERY ${ts} ms ──────────────────────────────────`,
      `  position   = read_sensors()   // ${sensorCount} sensors, ${spacing} mm apart`,
      `  error      = position − ${center}   // centre = (${sensorCount}−1)/2`,
      ``,
      `  if all sensors off line:`,
      `      error ← hold last extreme   // steer toward where line vanished`,
      ``,
      `  integral   ← integral + error × ${Ts}`,
      `  derivative ← (error − last_error) / ${Ts}`,
      ``,
      `  output = Kp × error  +  Ki × integral  +  Kd × derivative`,
      `         = ${kp} × error  +  ${ki} × integral  +  ${kd} × derivative`,
      `              ↑ P-term         ↑ I-term          ↑ D-term`,
      ``,
      `  left_motor  ← clamp(${base} − output,  ${lo}, ${hi})`,
      `  right_motor ← clamp(${base} + output,  ${lo}, ${hi})`,
      ``,
      `  last_error ← error`,
    ].join('\n')
  }

  return (
    <div className="code-panel">
      <div className="d-flex justify-content-between align-items-center mb-2">
        <ButtonGroup size="sm">
          {TABS.map((t, i) => (
            <Button key={t} variant={tab === i ? 'primary' : 'outline-secondary'}
              onClick={() => setTab(i)}>{t}</Button>
          ))}
        </ButtonGroup>
        {tab === 0 && <CopyBtn getText={pseudoText} />}
      </div>

      {tab === 0 && (
        <PseudoCode kp={kp} ki={ki} kd={kd} base={base} ts={ts}
          platform={platform} sensorCount={sensorCount} highlight={highlight} />
      )}
      {tab === 1 && (
        <ArduinoCode kp={kp} ki={ki} kd={kd} base={base} ts={ts}
          platform={platform} sensorCount={sensorCount} />
      )}
      {tab === 2 && (
        <MicroPythonCode kp={kp} ki={ki} kd={kd} base={base} ts={ts}
          sensorCount={sensorCount} />
      )}
    </div>
  )
}
